async def main():
    """主函数"""
    print("=" * 60)
    print("种子知识库初始化脚本")
    print("=" * 60)
    
    # 1. 连接数据库
    print("\n[1/4] 连接数据库...")
    engine = create_engine(settings.DATABASE_URL)
    Session = sessionmaker(bind=engine)
    db = Session()
    print(f"  ✓ 数据库连接成功")
    
    # 2. 检查是否已有种子数据
    print("\n[2/4] 检查现有数据...")
    existing = db.query(MvpKnowledgeItem).count()
    if existing > 0:
        print(f"  ⚠ 知识库已有 {existing} 条数据，跳过种子初始化")
        print("  如需重新初始化，请先清空 mvp_knowledge_items 表")
        db.close()
        return
    print(f"  ✓ 知识库为空，准备写入种子数据")
    
    # 3. 写入种子数据
    print("\n[3/4] 写入种子数据...")
    from app.services.embedding_service import EmbeddingService
    embedding_svc = EmbeddingService()
    
    success_count = 0
    embedding_success = 0
    embedding_failed = 0
    
    for i, data in enumerate(SEED_KNOWLEDGE):
        print(f"\n  [{i+1}/{len(SEED_KNOWLEDGE)}] {data['title'][:40]}...")
        
        try:
            # 创建知识条目
            item = MvpKnowledgeItem(**{k: v for k, v in data.items()})
            db.add(item)
            db.flush()
            
            # 生成 embedding 并创建 chunk
            embedding_text = f"{data['title']} {data.get('summary', '')} {data['content'][:500]}"
            
            try:
                embedding = await embedding_svc.generate_embedding(embedding_text)
                
                chunk = MvpKnowledgeChunk(
                    knowledge_id=item.id,
                    chunk_type="full",
                    chunk_index=0,
                    content=data["content"],
                    embedding=embedding,
                    token_count=len(data["content"]),
                )
                db.add(chunk)
                
                if embedding:
                    print(f"      ✓ Embedding 生成成功 (维度: {len(embedding)})")
                    embedding_success += 1
                else:
                    print(f"      ⚠ Embedding 返回 None")
                    embedding_failed += 1
                    
            except Exception as e:
                print(f"      ⚠ Embedding 生成失败: {e}")
                embedding_failed += 1
                # 仍然创建 chunk，只是没有 embedding
                chunk = MvpKnowledgeChunk(
                    knowledge_id=item.id,
                    chunk_type="full",
                    chunk_index=0,
                    content=data["content"],
                    token_count=len(data["content"]),
                )
                db.add(chunk)
            
            success_count += 1
            
        except Exception as e:
            print(f"      ✗ 写入失败: {e}")
            db.rollback()
            continue
    
    # 提交事务
    db.commit()
    
    # 4. 统计
    print("\n[4/4] 统计结果")
    print("=" * 60)
    print(f"✅ 成功写入 {success_count} 条种子知识")
    print(f"   - Embedding 成功: {embedding_success} 条")
    print(f"   - Embedding 失败: {embedding_failed} 条")
    print()
    
    for lib_type in ["hot_content", "audience_profile", "platform_rules", "compliance_rules", "prompt_templates"]:
        count = db.query(MvpKnowledgeItem).filter(MvpKnowledgeItem.library_type == lib_type).count()
        type_name = {
            "hot_content": "爆款内容",
            "audience_profile": "人群洞察",
            "platform_rules": "平台规则",
            "compliance_rules": "风险规则",
            "prompt_templates": "提示词模板",
        }.get(lib_type, lib_type)
        print(f"   {type_name}: {count} 条")
    
    print("=" * 60)
    print("种子知识库初始化完成！")
    print("=" * 60)
    
    db.close()


if __name__ == "__main__":
    asyncio.run(main())
